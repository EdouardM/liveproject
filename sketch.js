function setup() {
  
  createCanvas(400, 400);
}

function draw() {
  // Set background color 
  background(220);
  
  /*
  Draw red rectangle
  */
  // Red fill
  fill(255, 0, 0);

  // Black stroke
  stroke(0,0, 0);

  // Draw rectangle
  rect(110, 0, 60 , 60);


  
  // trait
  line(12,12,20,3);

}
